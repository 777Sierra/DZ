//Задайте массив заполненный случайными положительными трёхзначными числами. Напишите программу, которая покажет количество чётных чисел в массиве.

using System;

class Program
{
    static void Main(string[] args)
    {
        int[] array = new int[10];
        Random random = new Random();
        for (int i = 0; i < array.Length; i++)
        {
            array[i] = random.Next(100, 1000);
        }

        Console.Write("Массив: [");
        for (int i = 0; i < array.Length; i++)
        {
            Console.Write(array[i]);
            if (i != array.Length - 1)
            {
                Console.Write(", ");
            }
        }
        Console.WriteLine("]");

        int count = 0;
        for (int i = 0; i < array.Length; i++)
        {
            if (array[i] % 2 == 0)
            {
                count++;
            }
        }

        Console.WriteLine($"Количество чётных чисел в массиве: {count}");
    }
}

//В этом примере мы создаем массив из 10 элементов, заполняя его случайными положительными трехзначными числами. 
//Для генерации случайных чисел используется класс Random. Затем в цикле for происходит вывод элементов массива на экран, каждый элемент отделяется запятой и пробелом, за исключением последнего элемента. 
//В конце вывода на экран добавляются квадратные скобки. Затем происходит проверка каждого элемента массива на четность. 
//Если элемент является четным числом, то переменная count увеличивается на 1. 
//После окончания цикла на экран выводится количество четных чисел в массиве. Если нужен другой размер массива, то можно просто изменить значение переменной array.Length.