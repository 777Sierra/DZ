//Задайте одномерный массив, заполненный случайными числами. Найдите сумму элементов, стоящих на нечётных позициях.
using System;

class Program
{
    static void Main(string[] args)
    {
        int[] array = new int[10];
        Random random = new Random();
        for (int i = 0; i < array.Length; i++)
        {
            array[i] = random.Next(1, 100);
        }

        Console.Write("Массив: [");
        for (int i = 0; i < array.Length; i++)
        {
            Console.Write(array[i]);
            if (i != array.Length - 1)
            {
                Console.Write(", ");
            }
        }
        Console.WriteLine("]");

        int sum = 0;
        for (int i = 0; i < array.Length; i += 2)
        {
            sum += array[i];
        }

        Console.WriteLine($"Сумма элементов, стоящих на нечётных позициях: {sum}");
    }
}

//В этом примере мы создаем массив из 10 элементов, заполняя его случайными числами. Для генерации случайных чисел используется класс Random. 
//Затем в цикле for происходит вывод элементов массива на экран, каждый элемент отделяется запятой и пробелом, за исключением последнего элемента. 
//В конце вывода на экран добавляются квадратные скобки. 
//Затем происходит подсчет суммы элементов, стоящих на нечетных позициях (с индексами 1, 3, 5 и т.д.). 
